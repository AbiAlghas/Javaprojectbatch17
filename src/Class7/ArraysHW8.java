package Class7;

public class ArraysHW8 {
    /*
Create an array to store char values and then print those in reverse order
 */
    public static void main(String[] args) {
        char [] letters={'A','B','C','D','E'};
        for (int i = 4; i >=0 ; i--) {
            System.out.print(letters[i]+" ");

        }
    }
}
