package Review2;

public class E1Typecasting {
    public static void main(String[] args) {
        int number=(int)455.555;
        System.out.println(number);

        float number2=20.100f;
        double number3=number2;
        System.out.println(number3);

        //boolean bool=(boolean)12.5; error
        // by default every number considered as integers
        // type casting only can work with primitive data types


        char c=(char)35;
        System.out.println(c);




    }
}
