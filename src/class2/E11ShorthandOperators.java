package class2;

public class E11ShorthandOperators {
    public static void main(String[] args) {

        int number=10;
        number+=40;
        System.out.println(number);

        int area=100;
        area-=40;
        System.out.println(area);

        int age=10;
        age*=40;
        System.out.println(age);

        int money=40;
        money/=10;
        System.out.println(money);

        int num=100;
        num%=40;
        System.out.println(num);
        




    }
}
