package class3;

public class E5RelationalOperators {
    public static void main(String[] args) {
        System.out.println(10>15);  // is 10 greater than 15 =false
        System.out.println(10<15);  // is 10 smaller then 15 =true
        System.out.println(10==15); // is 10 equal to 15 =false
        System.out.println(10!=15); // is 10 not equal to 15 =true
        System.out.println(10>=15); //is 10 greater or equal to 15= false
        System.out.println(10<=15); //is 10 less or equal to 15= true (// if one answer is true the whole answer will be true


    }
}
