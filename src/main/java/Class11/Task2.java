package Class11;

public class Task2 {
    // Create a string and print in reverse order(sunday=yadnus)
    //important for interview as well
    public static void main(String[] args) {
        String day="Sunday";
        for (int i =day.length()-1;i>=0; i--) {
            System.out.print(day.charAt(i)+" ");



        }
    }
}
