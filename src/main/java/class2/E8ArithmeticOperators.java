package class2;

public class E8ArithmeticOperators {
    public static void main(String[] args) {
        int noOfSlices=5;
        int noOfFriends=2;
        System.out.println(noOfSlices/noOfFriends);
        System.out.println(noOfSlices%noOfFriends);


        int Slices=11;
        int Friends=3;
        System.out.println(Slices/Friends);
        System.out.println(Slices%Friends);


        double noSlices=11;
        double noFriends=3;
        System.out.println(noSlices/noFriends);
        System.out.println(noSlices%noFriends);

    }
}
